public class Dog {
    private String name;
    private String fullName;
    private byte age;

    public Dog(String name,String fullName,byte age){
        this.name=name;
        this.fullName=fullName;
        this.age=age;
    }
    public Dog() {

    }
    public String getName(){
    return name;
}
    public String getFullName(){
        return fullName;
    }
    public byte getAge(){
        return age;
}}
